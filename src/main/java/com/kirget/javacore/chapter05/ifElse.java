package com.kirget.javacore.chapter05;

public class ifElse {
    public static void main(String[] args) {
        int myInt = 10;
        if (myInt<5) {
            System.out.println(" Да верно ");
        }
        else if(myInt>15){
            System.out.println(" Нет не верно ");
        }
        else{
            System.out.println(" Ни один из вышеуказанных");
        }
    }
}
